using System;

namespace UnityEngine
{
	/// <summary>
	/// Description of Vector2.
	/// </summary>
	public struct Vector2
	{
		public float x;
		public float y;
		
		public Vector2(float x, float y)
		{
			this.x = x;
			this.y = y;
		}
	}
}
